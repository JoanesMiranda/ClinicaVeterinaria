    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        
        Veterinario vet = new Veterinario();
        
    //Pega as informações da tela "TelaVeterinario" e envia para a classe veterinario
        if(txtNome.getText().equals("")  || txtCrmv.getText().equals("") || txtCpf.getText().equals("   .   .   -  ") 
                || txtTelefone.getText().equals("(  )     -    ")){
            JOptionPane.showMessageDialog(null,"Campo de texto obrigatorio ");
        }else{
            vet.setNome(txtNome.getText());
            vet.setCrmv(txtCrmv.getText());
            vet.setCpf(txtCpf.getText());
            vet.setTelefone(txtTelefone.getText());
        
            VeterinarioControle vetControle = new VeterinarioControle();
        
        // para as informações para a classe "VeterinarioControle" e salva as informações no banco de dados na tabela Veterinario
            vetControle.salvarVeterinario(vet);
        }

    }//GEN-LAST:event_bntSalvarActionPerformed

